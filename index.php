<p class="font-weight-bold" style="margin-left:50px">Computer Engineering</p>
<p class="font-weight-light" style="margin-left:75px;margin-right:75px">Computer Engineering is a branch of engineering dealing with various aspects of computer hardware and software. The branch is closely associated with electronics engineering, as the underlying hardware comprises mainly of microprocessors, microcontrollers, logic gates etc. The engineers on graduation will be able to write software on various verticals of the business environment and would be able to carry out various scientific research projects. Today, with the advent of mobile computing devices, engineers are bridging the gap between artificial intelligence and human intelligence. Moreover, with the growth in research and development in the field of big data, cloud computing and algorithms ask for awareness, as computer engineering is going to be the next big lead in the domain of engineering.</p>

