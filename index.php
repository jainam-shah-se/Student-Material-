<p class="font-weight-bold" style="margin-left:50px">Mechanical Engineering</p>
<p class="font-weight-light" style="margin-left:75px;margin-right:75px">Bachelor of Technology in Mechanical Engineering is a Four year Programme, designed to have overall development of students and prepare them for direct induction in the Industries. Besides learning of basic courses of Mechanical Engineering, additional courses on skill development, application of technical learning, innovations and incubations, professional ethics, technical communication, entrepreneurship are included in the curriculum too.</p>

