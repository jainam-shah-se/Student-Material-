<p class="font-weight-light" style="margin-left:75px;margin-right:75px">An electrical engineer is someone who designs and develops new electrical systems, solves problems and tests equipment. They study and apply the physics and mathematics of electricity, electromagnetism and electronics to both large and small scale systems to process information and transmit energy. They work with all kinds of electronic devices, from the smallest pocket devices to large supercomputers. Electrical engineering students learn through a combination of design and lab work. This mix of theory and practical application allows students to visualize concepts and then apply their ideas in a variety of real life situations. Students learn to analyse and diagnose problems and develop innovative solutions.</p>
